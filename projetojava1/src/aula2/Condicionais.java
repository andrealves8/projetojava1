package aula2;

public class Condicionais {

	public static void main(String[] args) {
		int v1 = 5;
		int v2 = 10;

		if (v1 < v2) {
			System.out.println(v1 + " é menor que " + v2);
		} else {
			System.out.println(v1 + " é maior que " + v2);
		}
		
		/** < MENOR
		 *  > MAIOR
		 *  <= MENOR OU IGUAL
		 *  >= MAIOR OU IGUAL
		 */
	}
}
